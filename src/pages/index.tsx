import HeroBanner from '@/components/HeroBanner';
import Head from 'next/head';
import { sanityClient } from '../utils/sanity';
import { IBanner, IProduct } from 'typing';
import ProductList from '@/components/ProductList';

type Props = {
  dataBanner: IBanner;
  dataProduct: IProduct[];
};

export default function Home({ dataBanner, dataProduct }: Props) {
  return (
    <>
      <Head>
        <title>Shop</title>
        <meta
          name='description'
          content='E-commerce app Generated by create next app'
        />
        <meta name='viewport' content='width=device-width, initial-scale=1' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <main className='flex flex-col items-center bg-base-300'>
        <HeroBanner data={dataBanner} />
        <ProductList data={dataProduct} />
      </main>
    </>
  );
}

export const getServerSideProps = async () => {
  const queryBanner = `*[_type == "banner" ][0]{
      buttonText, 
      desc, 
      discount, 
      image, 
      largeText1, 
      largeText2, 
      midText, 
      product, 
      saletime, 
      smallText
    }`;
  const queryProduct = `*[_type == "product" ]{
    _id,
    image,
    name,
    price,
    slug,
    details
  }`;

  const dataBanner = await sanityClient.fetch(queryBanner);
  const dataProduct = await sanityClient.fetch(queryProduct);
  return {
    props: {
      dataBanner,
      dataProduct
    }
  };
};
